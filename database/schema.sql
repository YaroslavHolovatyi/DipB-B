CREATE TABLE conversations (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    type            conversation_type NOT NULL,
    -- Group chats may be backed by an existing friend_group (1:1 nullable link)
    friend_group_id BIGINT             UNIQUE REFERENCES friend_groups(id) ON DELETE SET NULL,
