CREATE TABLE bars (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    slug            TEXT            NOT NULL UNIQUE,
    name            TEXT            NOT NULL,
    city_id         BIGINT          NOT NULL REFERENCES cities(id) ON DELETE RESTRICT,

    address         TEXT,
    location        GEOGRAPHY(POINT, 4326),

    description     TEXT,
    phone           TEXT,
    website         TEXT,
    image_url       TEXT,

    price_category  price_category  NOT NULL DEFAULT 'mid',

    -- Denormalized rating cache, refreshed via trigger / job
    rating_avg      NUMERIC(3,2)    NOT NULL DEFAULT 0.00
                                    CHECK (rating_avg >= 0 AND rating_avg <= 5),
    rating_count    INTEGER         NOT NULL DEFAULT 0
                                    CHECK (rating_count >= 0),

    -- Weekly schedule, e.g. {"mon":{"open":"12:00","close":"23:00"}, ...}
    work_hours      JSONB           NOT NULL DEFAULT '{}'::jsonb,

    -- Full-text search vector (name + description + address), maintained by trigger
    search_vector   tsvector,

    is_active       BOOLEAN         NOT NULL DEFAULT TRUE,
    created_at      TIMESTAMPTZ     NOT NULL DEFAULT now(),
    updated_at      TIMESTAMPTZ     NOT NULL DEFAULT now(),
    deleted_at      TIMESTAMPTZ
);

CREATE INDEX idx_bars_city          ON bars(city_id);
CREATE INDEX idx_bars_price         ON bars(price_category);
CREATE INDEX idx_bars_rating        ON bars(rating_avg DESC) WHERE is_active AND deleted_at IS NULL;
CREATE INDEX idx_bars_location      ON bars USING GIST (location);
CREATE INDEX idx_bars_search        ON bars USING GIN  (search_vector);
CREATE INDEX idx_bars_name_trgm     ON bars USING GIN  (name gin_trgm_ops);

CREATE FUNCTION bars_search_vector_update() RETURNS TRIGGER AS $$
BEGIN
    NEW.search_vector :=
        setweight(to_tsvector('simple', coalesce(NEW.name,       '')), 'A') ||
        setweight(to_tsvector('simple', coalesce(NEW.description,'')), 'B') ||
        setweight(to_tsvector('simple', coalesce(NEW.address,    '')), 'C');
    NEW.updated_at := now();
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER bars_search_vector_trigger
    BEFORE INSERT OR UPDATE OF name, description, address
    ON bars
    FOR EACH ROW EXECUTE FUNCTION bars_search_vector_update();

