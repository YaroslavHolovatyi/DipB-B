    created_at      TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at      TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE INDEX idx_conversations_last_message ON conversations(last_message_at DESC NULLS LAST);

CREATE TABLE conversation_participants (
    conversation_id BIGINT             NOT NULL REFERENCES conversations(id) ON DELETE CASCADE,
    user_id         BIGINT             NOT NULL REFERENCES users(id)         ON DELETE CASCADE,
    role               conversation_role  NOT NULL DEFAULT 'member',
    is_muted           BOOLEAN            NOT NULL DEFAULT FALSE,
    last_delivered_at  TIMESTAMPTZ,         -- newest message confirmed reaching this user's device
    last_read_at       TIMESTAMPTZ,         -- newest message this user has read
    joined_at          TIMESTAMPTZ        NOT NULL DEFAULT now(),
    left_at            TIMESTAMPTZ,
    PRIMARY KEY (conversation_id, user_id)
);

CREATE INDEX idx_conversation_participants_user
    ON conversation_participants(user_id)
    WHERE left_at IS NULL;

CREATE TABLE messages (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    conversation_id BIGINT      NOT NULL REFERENCES conversations(id) ON DELETE CASCADE,
    sender_id       BIGINT      NOT NULL REFERENCES users(id)         ON DELETE CASCADE,
    body            TEXT,
    -- [{url, type, name, size}] for images / files attached to a message
    attachments     JSONB       NOT NULL DEFAULT '[]'::jsonb,
    reply_to_id     BIGINT               REFERENCES messages(id) ON DELETE SET NULL,
    edited_at       TIMESTAMPTZ,
    deleted_at      TIMESTAMPTZ,
    created_at      TIMESTAMPTZ NOT NULL DEFAULT now(),

    -- a message must carry text OR at least one attachment
    CONSTRAINT chk_messages_has_content
        CHECK (body IS NOT NULL OR jsonb_array_length(attachments) > 0)
);

CREATE INDEX idx_messages_conversation ON messages(conversation_id, created_at DESC);
CREATE INDEX idx_messages_sender       ON messages(sender_id);

-- Optional emoji reactions on a message
CREATE TABLE message_reactions (
    message_id  BIGINT      NOT NULL REFERENCES messages(id) ON DELETE CASCADE,
    user_id     BIGINT      NOT NULL REFERENCES users(id)    ON DELETE CASCADE,
    emoji       TEXT        NOT NULL,
    created_at  TIMESTAMPTZ NOT NULL DEFAULT now(),
    PRIMARY KEY (message_id, user_id, emoji)
);

-- Live presence used by the chat list (green dot) — updated by the WebSocket layer
CREATE TABLE user_presence (
    user_id      BIGINT          PRIMARY KEY REFERENCES users(id) ON DELETE CASCADE,
    status       presence_status NOT NULL DEFAULT 'offline',
    last_seen_at TIMESTAMPTZ     NOT NULL DEFAULT now()
);

-- Bump conversations.last_message_at on every new message so chat lists sort cheaply
CREATE FUNCTION conversations_touch_last_message() RETURNS TRIGGER AS $$
BEGIN
    UPDATE conversations
       SET last_message_at = NEW.created_at,
           updated_at      = now()
     WHERE id = NEW.conversation_id;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trg_messages_touch_conversation
    AFTER INSERT ON messages
    FOR EACH ROW EXECUTE FUNCTION conversations_touch_last_message();


