-- =====================================================================
-- Beer & Beverages  —  PostgreSQL 16 schema
-- Project: "Довідка пабів м. Львів" (mobile rebuild)
-- Stack:   React Native + Expo  /  FastAPI  /  PostgreSQL + PostGIS
--
-- Conventions
--   * BIGINT identity PKs (matches old Hibernate generation)
--   * snake_case table & column names
--   * TIMESTAMPTZ everywhere — server is UTC, clients convert
--   * citext for case-insensitive email / username uniqueness
--   * Soft delete via deleted_at on user-facing entities
--   * GEOGRAPHY(POINT, 4326) for "real-world" lat/lon (PostGIS)
--   * JSONB for OCR payloads, push data, achievement requirements
--   * Junction tables use composite PKs
-- =====================================================================

