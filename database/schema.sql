-- =====================================================================
-- 7. Checks / receipts and split
-- =====================================================================

CREATE TABLE checks (
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id             BIGINT       NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    bar_id              BIGINT                REFERENCES bars(id)  ON DELETE SET NULL,
    -- when the bar isn't in our catalog, capture freeform name from receipt
    bar_name_freeform   TEXT,
    currency            CHAR(3)      NOT NULL DEFAULT 'UAH',
    total_amount        NUMERIC(12,2) NOT NULL CHECK (total_amount >= 0),
    image_url           TEXT,
    -- Raw response from OpenAI Vision (or alternative OCR)
    ocr_payload         JSONB        NOT NULL DEFAULT '{}'::jsonb,
    parsed_at           TIMESTAMPTZ,
    occurred_at         TIMESTAMPTZ,                                 -- the date on the receipt
    note                TEXT,
    created_at          TIMESTAMPTZ  NOT NULL DEFAULT now(),
    updated_at          TIMESTAMPTZ  NOT NULL DEFAULT now(),
    deleted_at          TIMESTAMPTZ
);

CREATE INDEX idx_checks_user      ON checks(user_id, created_at DESC) WHERE deleted_at IS NULL;
CREATE INDEX idx_checks_bar       ON checks(bar_id);

CREATE TABLE check_items (
    id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    check_id      BIGINT        NOT NULL REFERENCES checks(id) ON DELETE CASCADE,
    name          TEXT          NOT NULL,
    quantity      NUMERIC(10,2) NOT NULL DEFAULT 1 CHECK (quantity > 0),
    unit_price    NUMERIC(12,2) NOT NULL CHECK (unit_price >= 0),
    total_price   NUMERIC(12,2) NOT NULL CHECK (total_price >= 0),
    position      SMALLINT      NOT NULL DEFAULT 0
);

CREATE INDEX idx_check_items_check ON check_items(check_id, position);

-- Participants in a split. May be a registered user (invited via the room flow)
-- or just a guest name added manually by the host.
CREATE TABLE check_participants (
    id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    check_id      BIGINT                   NOT NULL REFERENCES checks(id) ON DELETE CASCADE,
    user_id       BIGINT                            REFERENCES users(id)  ON DELETE SET NULL,
    display_name  TEXT                     NOT NULL,
    color         CHAR(7) CHECK (color IS NULL OR color ~ '^#[0-9A-Fa-f]{6}$'),  -- "#RRGGBB" for UI

    -- Split-room lifecycle:
    --   'invited' — registered user, notified, not yet in the room
    --   'joined'  — actively marking items (also the default for guests)
    --   'ready'   — finished marking; waiting for the room to close
    --   'left'    — declined invitation or left the room
    status        check_participant_status NOT NULL DEFAULT 'invited',
    joined_at     TIMESTAMPTZ,
    ready_at      TIMESTAMPTZ,
    left_at       TIMESTAMPTZ,

    created_at    TIMESTAMPTZ              NOT NULL DEFAULT now(),

    UNIQUE (check_id, display_name),
    -- Guests are joined immediately; registered users start invited.
    CONSTRAINT chk_check_participants_guest_status
        CHECK (user_id IS NOT NULL OR status IN ('joined', 'left'))
);

CREATE INDEX idx_check_participants_user_status
    ON check_participants(user_id, status)
    WHERE user_id IS NOT NULL;

CREATE INDEX idx_check_participants_check ON check_participants(check_id);
CREATE INDEX idx_check_participants_user  ON check_participants(user_id);

CREATE TABLE check_item_assignments (
    check_item_id   BIGINT        NOT NULL REFERENCES check_items(id)        ON DELETE CASCADE,
    participant_id  BIGINT        NOT NULL REFERENCES check_participants(id) ON DELETE CASCADE,
    quantity        NUMERIC(10,2) NOT NULL DEFAULT 1 CHECK (quantity > 0),
    amount          NUMERIC(12,2) NOT NULL CHECK (amount >= 0),
    PRIMARY KEY (check_item_id, participant_id)
);

CREATE INDEX idx_check_assignments_participant ON check_item_assignments(participant_id);

-- "Choose One to Pay for All" requires unanimous consent from all registered
-- participants in the split. A proposal records that consent process; the
-- D20 dice game only runs if every vote is 'accept'.
CREATE TABLE dice_proposals (
    id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    check_id     BIGINT                NOT NULL REFERENCES checks(id) ON DELETE CASCADE,
    proposed_by  BIGINT                NOT NULL REFERENCES users(id)  ON DELETE CASCADE,
    status       dice_proposal_status  NOT NULL DEFAULT 'pending',
    decided_at   TIMESTAMPTZ,
    -- when status='cancelled' (e.g. a voter left the room), why
    cancel_reason TEXT,
    created_at   TIMESTAMPTZ           NOT NULL DEFAULT now()
);

-- Only one pending proposal per check at a time.
CREATE UNIQUE INDEX uq_dice_proposals_pending
    ON dice_proposals(check_id) WHERE status = 'pending';
CREATE INDEX idx_dice_proposals_check ON dice_proposals(check_id, created_at DESC);

-- One vote row per registered participant per proposal.
-- A row is inserted with vote='pending' when the proposal is created.
CREATE TABLE dice_proposal_votes (
    proposal_id  BIGINT       NOT NULL REFERENCES dice_proposals(id) ON DELETE CASCADE,
    user_id      BIGINT       NOT NULL REFERENCES users(id)          ON DELETE CASCADE,
    vote         dice_vote    NOT NULL DEFAULT 'pending',
    voted_at     TIMESTAMPTZ,
    PRIMARY KEY (proposal_id, user_id)
);

CREATE INDEX idx_dice_proposal_votes_user ON dice_proposal_votes(user_id);

-- D20 dice "Choose One to Pay for All" outcomes
-- Drives the "Добра Душа" (Kind Soul) leaderboard. A row is inserted only
-- after a dice_proposals row reaches status='accepted' and the dice is rolled.
CREATE TABLE kind_soul_events (
    id                     BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    check_id               BIGINT                NOT NULL REFERENCES checks(id) ON DELETE CASCADE,
    proposal_id            BIGINT                         REFERENCES dice_proposals(id) ON DELETE SET NULL,
    payer_user_id          BIGINT                NOT NULL REFERENCES users(id)  ON DELETE CASCADE,
    payer_participant_id   BIGINT                         REFERENCES check_participants(id) ON DELETE SET NULL,
    total_paid_for_others  NUMERIC(12,2)         NOT NULL CHECK (total_paid_for_others >= 0),
    decided_via            check_payment_method  NOT NULL DEFAULT 'd20_dice',
    -- per-participant dice rolls and any other audit data
    metadata               JSONB                 NOT NULL DEFAULT '{}'::jsonb,
    occurred_at            TIMESTAMPTZ           NOT NULL DEFAULT now(),

    UNIQUE (check_id)   -- one kind-soul event per check
);

CREATE INDEX idx_kind_soul_user ON kind_soul_events(payer_user_id, occurred_at DESC);


