    requirement       JSONB                   NOT NULL DEFAULT '{}'::jsonb,
    is_active         BOOLEAN                 NOT NULL DEFAULT TRUE,
    created_at        TIMESTAMPTZ             NOT NULL DEFAULT now()
);

CREATE INDEX idx_achievements_category ON achievements(category) WHERE is_active;
CREATE INDEX idx_achievements_race     ON achievements(race_id);

