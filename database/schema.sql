-- 11b. Triggers that maintain denormalized caches
-- =====================================================================

-- Keep bars.rating_avg / rating_count in sync with bar_reviews. Fires on every
-- review insert / update / delete and recomputes the cached values for that bar
-- in a single statement. AVG returns NULL when the bar has no reviews left,
-- so we COALESCE to 0.
CREATE FUNCTION bars_refresh_rating_cache() RETURNS TRIGGER AS $$
DECLARE
    target_bar BIGINT;
BEGIN
    target_bar := COALESCE(NEW.bar_id, OLD.bar_id);
    UPDATE bars
       SET rating_avg   = COALESCE((SELECT ROUND(AVG(rating)::numeric, 2)
                                      FROM bar_reviews
                                     WHERE bar_id = target_bar), 0),
           rating_count = (SELECT COUNT(*)
                             FROM bar_reviews
                            WHERE bar_id = target_bar)
     WHERE id = target_bar;
    RETURN NULL;          -- AFTER trigger, return value is ignored
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trg_bar_reviews_refresh_cache
    AFTER INSERT OR UPDATE OR DELETE
    ON bar_reviews
    FOR EACH ROW EXECUTE FUNCTION bars_refresh_rating_cache();


